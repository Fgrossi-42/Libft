#include "../Include/libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t	i;

	i = 0;
	while (len > i)
	{
		((unsigned char *)b)[i] = c;
		i++;
	}
	return ((unsigned char *)b);
}
